#include <stdio.h>
/**
 * main - prints number of arguments passed to the function
 * @argc: number of arguments passed to the function
 * @argv: arguments passed to the function
 *
 * Return: 0 on success
 */
int main(int argc, char __attribute__ ((unused)) *argv[])
{
	printf("%d\n", argc);
	return(0);
}
